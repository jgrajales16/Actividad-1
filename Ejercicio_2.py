suma = 0
# Asignamos el valor 20 a X
x = 20
# Sumamos X a suma
suma = suma + x
# Asignamos el valor 40 a Y
y = 40
# Calculamos X + Y elevado al cuadrado y actualizamos el valor de X
x = x + y ** 2
# Calculamos la suma final con X dividido entre Y
suma = suma + x / y
# Mostramos los resultados
print("El valor de la suma es:", suma)
